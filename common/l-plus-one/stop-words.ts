/** Mapa de idiomas a sus stop words */
export const STOP_WORDS_BY_LANGUAGE: Record<string, Set<string>> = {
    en: ENGLISH_STOP_WORDS,
    es: SPANISH_STOP_WORDS,
    ja: JAPANESE_STOP_WORDS,
    // Agregar más idiomas según sea necesario
};

/**
 * Obtener stop words para un idioma
 * @param language Código ISO del idioma
 * @returns Set de stop words (vacío si no hay datos)
 */
export function getStopWords(language: string): Set<string> {
    return STOP_WORDS_BY_LANGUAGE[language] || new Set();
}

/**
 * Verificar si una palabra es stop word
 * @param word Palabra a verificar
 * @param language Código ISO del idioma
 */
export function isStopWord(word: string, language: string): boolean {
    const stopWords = getStopWords(language);
    return stopWords.has(word.toLowerCase());
}
